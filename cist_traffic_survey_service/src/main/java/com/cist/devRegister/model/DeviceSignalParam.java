package com.cist.devRegister.model;

public class DeviceSignalParam {
	//设备ID 
	private int  device_id;
	//是否联网（0：否；1：是）
	private char  has_network;
	//是否接入指挥中心（0：否；1：是）
	private char  access_command_center;
	
	
	
	public DeviceSignalParam() {
	}
	public int getDevice_id() {
		return device_id;
	}
	public void setDevice_id(int device_id) {
		this.device_id = device_id;
	}
	public char getHas_network() {
		return has_network;
	}
	public void setHas_network(char has_network) {
		this.has_network = has_network;
	}
	public char getAccess_command_center() {
		return access_command_center;
	}
	public void setAccess_command_center(char access_command_center) {
		this.access_command_center = access_command_center;
	}
	
	
	
}
