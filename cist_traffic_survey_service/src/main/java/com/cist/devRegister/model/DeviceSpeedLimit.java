package com.cist.devRegister.model;
//	5.7.测速(JCSJ_DEVICE_SPEED_LIMIT)
public class DeviceSpeedLimit {
	// 设备ID
	private int device_id;
	// 最大限速
	private int maximum_speed_limit;
	//最小限速
	private int minimum_speed_limit;
	
	
	public DeviceSpeedLimit() {

	}
	public int getDevice_id() {
		return device_id;
	}
	public void setDevice_id(int device_id) {
		this.device_id = device_id;
	}
	public int getMaximum_speed_limit() {
		return maximum_speed_limit;
	}
	public void setMaximum_speed_limit(int maximum_speed_limit) {
		this.maximum_speed_limit = maximum_speed_limit;
	}
	public int getMinimum_speed_limit() {
		return minimum_speed_limit;
	}
	public void setMinimum_speed_limit(int minimum_speed_limit) {
		this.minimum_speed_limit = minimum_speed_limit;
	}
	
	
	
}
