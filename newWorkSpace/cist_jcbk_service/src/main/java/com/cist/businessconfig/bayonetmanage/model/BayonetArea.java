public class BayonetArea {
	private Long pk	;//主键
	private String kkbh	;//卡口编号
	private Long bkkk	;//布控卡口PK（关联JCBK_CONTROL_BAYONET表中PK字段）
	private Long qypk	;//区域PK（关联JCBK_FENCE_AREA表中PK字段）
	public Long getPk() {
		return pk;
	}
	public void setPk(Long pk) {
		this.pk = pk;
	}
	public String getKkbh() {
		return kkbh;
	}
	public void setKkbh(String kkbh) {
		this.kkbh = kkbh;
	}
	public Long getBkkk() {
		return bkkk;
	}
	public void setBkkk(Long bkkk) {
		this.bkkk = bkkk;
	}
	public Long getQypk() {
		return qypk;
	}
	public void setQypk(Long qypk) {
		this.qypk = qypk;
	}
	
	
	
}
