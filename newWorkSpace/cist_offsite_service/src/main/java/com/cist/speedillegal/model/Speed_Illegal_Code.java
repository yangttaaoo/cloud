package com.cist.speedillegal.model;

import java.io.Serializable;

/**
 * 超速违法代码配置表
 * @author hy
 *
 */
public class Speed_Illegal_Code implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -4169001903981383383L;
	private Integer pk;//ID
	private String vehicle_type;//车辆类型
	private String rode_id;//道路id（对应c_road_level表）
	private String speed_proportion;//超速比例
	private Integer illegal_id;//违法id(对应tbl_offe_cate表)
	private String remarks;//备注
	public Integer getPk() {
		return pk;
	}
	public void setPk(Integer pk) {
		this.pk = pk;
	}
	public String getVehicle_type() {
		return vehicle_type;
	}
	public void setVehicle_type(String vehicle_type) {
		this.vehicle_type = vehicle_type;
	}
	public String getRode_id() {
		return rode_id;
	}
	public void setRode_id(String rode_id) {
		this.rode_id = rode_id;
	}
	public String getSpeed_proportion() {
		return speed_proportion;
	}
	public void setSpeed_proportion(String speed_proportion) {
		this.speed_proportion = speed_proportion;
	}
	public Integer getIllegal_id() {
		return illegal_id;
	}
	public void setIllegal_id(Integer illegal_id) {
		this.illegal_id = illegal_id;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	
}
