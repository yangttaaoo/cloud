package com.cist.redlist.model;

public class CarColorInfo {
	private String pk;//车身颜色pk
	private String code;//车身颜色code
	private String name;//车身颜色名称
	public String getPk() {
		return pk;
	}
	public void setPk(String pk) {
		this.pk = pk;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
