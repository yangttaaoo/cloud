package com.cist.devRegister.model;
//5.10.诱导屏参数(JCSJ_DEVICE_LED_PARAM)
public class DeviceLedParam {
	//设备ID
	private int device_id;
	//诱导类型ID（大型LED交通诱导屏、光带型交通状态显示屏、停车诱导屏、其他）
	private int led_type_id;
	//	发布方式ID（无线发布、网络发布）
	private int release_mode_id;
	//高度
	private int height;
	//宽度
	private int wight;
	//分辨率
	private int resolution;
	//点间距规格ID（16mm、18mm、20mm、22mm、25mm）
	private int point_spacing_id;
	//显示性能ID（文字、图片、视频）
	private int display_performance_id;
	//	图像分布ID（全屏诱导、局部诱导）
	private int image_distribution_id;
	//设备规格（参考SYS_FRM_CODE表 FCT_CODE=SBGG)
	private String led_mode;
	//屏幕尺寸（参考SYS_FRM_CODE表 FCT_CODE=L009)
	private String led_size;
	
	
	
	public String getLed_mode() {
		return led_mode;
	}
	public void setLed_mode(String led_mode) {
		this.led_mode = led_mode;
	}
	public String getLed_size() {
		return led_size;
	}
	public void setLed_size(String led_size) {
		this.led_size = led_size;
	}
	public DeviceLedParam() {
	}
	public int getDevice_id() {
		return device_id;
	}
	public void setDevice_id(int device_id) {
		this.device_id = device_id;
	}
	public int getLed_type_id() {
		return led_type_id;
	}
	public void setLed_type_id(int led_type_id) {
		this.led_type_id = led_type_id;
	}
	public int getRelease_mode_id() {
		return release_mode_id;
	}
	public void setRelease_mode_id(int release_mode_id) {
		this.release_mode_id = release_mode_id;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWight() {
		return wight;
	}
	public void setWight(int wight) {
		this.wight = wight;
	}
	public int getResolution() {
		return resolution;
	}
	public void setResolution(int resolution) {
		this.resolution = resolution;
	}
	public int getPoint_spacing_id() {
		return point_spacing_id;
	}
	public void setPoint_spacing_id(int point_spacing_id) {
		this.point_spacing_id = point_spacing_id;
	}
	public int getDisplay_performance_id() {
		return display_performance_id;
	}
	public void setDisplay_performance_id(int display_performance_id) {
		this.display_performance_id = display_performance_id;
	}
	public int getImage_distribution_id() {
		return image_distribution_id;
	}
	public void setImage_distribution_id(int image_distribution_id) {
		this.image_distribution_id = image_distribution_id;
	}
	
	
	
}
