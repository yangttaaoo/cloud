package com.cist.role.model;

import java.io.Serializable;

public class RoleInfo implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer syri_pk;
	private String syri_name;
	private String syri_flag;
	private String syri_flag_name;
	private String remark;
	private char syri_level;
	private String syri_buider;
	private Integer dept_pk;
	
	
	public String getSyri_flag_name() {
		return syri_flag_name;
	}
	public void setSyri_flag_name(String syri_flag_name) {
		this.syri_flag_name = syri_flag_name;
	}
	public Integer getSyri_pk() {
		return syri_pk;
	}
	public void setSyri_pk(Integer syri_pk) {
		this.syri_pk = syri_pk;
	}
	public String getSyri_name() {
		return syri_name;
	}
	public void setSyri_name(String syri_name) {
		this.syri_name = syri_name;
	}
	public String getSyri_flag() {
		return syri_flag;
	}
	public void setSyri_flag(String syri_flag) {
		this.syri_flag = syri_flag;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public char getSyri_level() {
		return syri_level;
	}
	public void setSyri_level(char syri_level) {
		this.syri_level = syri_level;
	}
	public String getSyri_buider() {
		return syri_buider;
	}
	public void setSyri_buider(String syri_buider) {
		this.syri_buider = syri_buider;
	}
	public Integer getDept_pk() {
		return dept_pk;
	}
	public void setDept_pk(Integer dept_pk) {
		this.dept_pk = dept_pk;
	}
	
	
}
