package com.cist.assessStatisticalRelease.assessTotalityAnalysis.model;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

public class AppraiseRegulations {
	// ID
	private Long pk_id;
	// 考核栏目ID
	private Long fk_appraise_topic_id;
	// 条例名称
	private String regulations_name;
	// 评定规则（0：零分；1：加分；2：减分）
	private Character assessment_rules;
	// 评定分值
	private Integer assessment_num;
	// 条例说明
	private String topic_explain;
	// 启用状态（0：禁用；1：启用）
	private Character enabled_state;
	// 创建人
	private Long create_person_id;
	// 创建时间
	@JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss", timezone = "GMT+8")
	private Date create_time;
	
	private Long test;
	
	
	public Long getTest() {
		return test;
	}
	public void setTest(Long test) {
		this.test = test;
	}
	public Long getPk_id() {
		return pk_id;
	}
	public void setPk_id(Long pk_id) {
		this.pk_id = pk_id;
	}
	public Long getFk_appraise_topic_id() {
		return fk_appraise_topic_id;
	}
	public void setFk_appraise_topic_id(Long fk_appraise_topic_id) {
		this.fk_appraise_topic_id = fk_appraise_topic_id;
	}
	public String getRegulations_name() {
		return regulations_name;
	}
	public void setRegulations_name(String regulations_name) {
		this.regulations_name = regulations_name;
	}
	public Character getAssessment_rules() {
		return assessment_rules;
	}
	public void setAssessment_rules(Character assessment_rules) {
		this.assessment_rules = assessment_rules;
	}
	public Integer getAssessment_num() {
		return assessment_num;
	}
	public void setAssessment_num(Integer assessment_num) {
		this.assessment_num = assessment_num;
	}
	public String getTopic_explain() {
		return topic_explain;
	}
	public void setTopic_explain(String topic_explain) {
		this.topic_explain = topic_explain;
	}
	public Character getEnabled_state() {
		return enabled_state;
	}
	public void setEnabled_state(Character enabled_state) {
		this.enabled_state = enabled_state;
	}
	public Long getCreate_person_id() {
		return create_person_id;
	}
	public void setCreate_person_id(Long create_person_id) {
		this.create_person_id = create_person_id;
	}
	public Date getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

}
