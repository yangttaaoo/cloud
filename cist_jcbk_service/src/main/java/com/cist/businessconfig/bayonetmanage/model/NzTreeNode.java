package com.cist.businessconfig.bayonetmanage.model;

import java.util.List;

public class NzTreeNode{
	private String title;
	private String key;
	private List<NzTreeNode> children = null;
	private Boolean selectable = false;// 是否可以选中
	private Boolean expanded = false;//
	private Boolean isLeaf = false;// 是否为叶子节点
	
	private String device_num;
	private String device_name;
	private String road_name;
	private String longitude;
	private String latitude;
	private String fk_dept_id;
	private String bayonet_name;
	

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}






	public List<NzTreeNode> getChildren() {
		return children;
	}

	public void setChildren(List<NzTreeNode> children) {
		this.children = children;
	}

	public Boolean getSelectable() {
		return selectable;
	}

	public void setSelectable(Boolean selectable) {
		this.selectable = selectable;
	}

	public Boolean getExpanded() {
		return expanded;
	}

	public void setExpanded(Boolean expanded) {
		this.expanded = expanded;
	}

	public Boolean getIsLeaf() {
		return isLeaf;
	}

	public void setIsLeaf(Boolean isLeaf) {
		this.isLeaf = isLeaf;
	}

	public String getDevice_num() {
		return device_num;
	}

	public void setDevice_num(String device_num) {
		this.device_num = device_num;
	}

	public String getRoad_name() {
		return road_name;
	}

	public void setRoad_name(String road_name) {
		this.road_name = road_name;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getFk_dept_id() {
		return fk_dept_id;
	}

	public void setFk_dept_id(String fk_dept_id) {
		this.fk_dept_id = fk_dept_id;
	}

	public String getDevice_name() {
		return device_name;
	}

	public void setDevice_name(String device_name) {
		this.device_name = device_name;
	}

	public String getBayonet_name() {
		return bayonet_name;
	}

	public void setBayonet_name(String bayonet_name) {
		this.bayonet_name = bayonet_name;
	}

}
