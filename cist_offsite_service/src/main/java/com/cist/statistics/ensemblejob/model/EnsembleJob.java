package com.cist.statistics.ensemblejob.model;

import java.io.Serializable;
import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

/**
 * 
 * @author hy
 *
 */
public class EnsembleJob implements Serializable{
		/**
	 * 
	 */
	private static final long serialVersionUID = -3560822869525054492L;
		@JsonFormat(pattern="yyyy-MM-dd",timezone="GMT+8")
		private Date sj;//时间
		private String wfxw;//违法行为
		private Integer wfxwlxsl;//违法类型数量
		private Integer zpsl;//抓拍数量
		private Integer shsl;//审核数量(包括成功和失败的)
		private Integer tgsl;//通过数量
		@JsonFormat(pattern="yyyy-MM-dd HH:mm:ss",timezone="GMT+8")
		private Date kssj;//开始时间
		@JsonFormat(pattern="yyyy-MM-dd HH:mm:ss",timezone="GMT+8")
		private Date jssj;//结束时间
		private String cjdw;//采集单位
		public Date getSj() {
			return sj;
		}
		public void setSj(Date sj) {
			this.sj = sj;
		}
		public String getWfxw() {
			return wfxw;
		}
		public void setWfxw(String wfxw) {
			this.wfxw = wfxw;
		}
		public Integer getZpsl() {
			return zpsl;
		}
		public void setZpsl(Integer zpsl) {
			this.zpsl = zpsl;
		}
		public Integer getShsl() {
			return shsl;
		}
		public void setShsl(Integer shsl) {
			this.shsl = shsl;
		}
		public Date getKssj() {
			return kssj;
		}
		public void setKssj(Date kssj) {
			this.kssj = kssj;
		}
		public Date getJssj() {
			return jssj;
		}
		public void setJssj(Date jssj) {
			this.jssj = jssj;
		}
		public String getCjdw() {
			return cjdw;
		}
		public void setCjdw(String cjdw) {
			this.cjdw = cjdw;
		}
		public Integer getTgsl() {
			return tgsl;
		}
		public void setTgsl(Integer tgsl) {
			this.tgsl = tgsl;
		}
		public Integer getWfxwlxsl() {
			return wfxwlxsl;
		}
		public void setWfxwlxsl(Integer wfxwlxsl) {
			this.wfxwlxsl = wfxwlxsl;
		}
		
}
