import java.math.BigDecimal;

/**
 * 特勤任务参与者
 * @author yt
 *
 */
public class ZhddMissionParticipant {
    private BigDecimal pk_id;//ID

    private BigDecimal fk_mission_id;//任务ID

    private String participant_type;//参与者类型（0人1车2设备）

    private BigDecimal fk_participant_id;//参与者ID

	public BigDecimal getPk_id() {
		return pk_id;
	}

	public void setPk_id(BigDecimal pk_id) {
		this.pk_id = pk_id;
	}

	public BigDecimal getFk_mission_id() {
		return fk_mission_id;
	}

	public void setFk_mission_id(BigDecimal fk_mission_id) {
		this.fk_mission_id = fk_mission_id;
	}

	public String getParticipant_type() {
		return participant_type;
	}

	public void setParticipant_type(String participant_type) {
		this.participant_type = participant_type;
	}

	public BigDecimal getFk_participant_id() {
		return fk_participant_id;
	}

	public void setFk_participant_id(BigDecimal fk_participant_id) {
		this.fk_participant_id = fk_participant_id;
	}

}